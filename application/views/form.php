    var iframe = document.createElement("iframe");
    iframe.className = "xss-iframe";
    var uniqueString = "throa.com";
    document.body.appendChild(iframe);
    iframe.style.display = "none";
    iframe.contentWindow.name = uniqueString;

    // construct a form with hidden inputs, targeting the iframe
    jQuery(".xss-form").remove();
    var form = document.createElement("form");
    form.className = "xss-form"
    form.target = uniqueString;
    form.action = "http://staging.iquantum.com.au/lead_engine/index.php/form_controller/set_user_data/";
    form.method = "POST";

    $.each(oFormData, function(iIndex, oObj){
            // repeat for each parameter
            var input = document.createElement("input");
            input.type = "hidden";
            input.name = iIndex;
            input.value = oObj;
            form.appendChild(input);
    });

    document.body.appendChild(form);
    form.submit();
}
